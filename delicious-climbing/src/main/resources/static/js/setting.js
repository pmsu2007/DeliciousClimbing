$("#searchKeyword").keyup(function(e) {
    var content = $(this).val();
    $("#textLengthCheck").val("" + content.length + " / 20"); //실시간 글자수 카운팅
    if (content.length > 20) {
        Alert("최대 20자까지 입력 가능합니다.");
        $(this).val(content.substring(0, 20));
    }
});

